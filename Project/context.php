        <h2>People are experiencing climate change in diverse ways</h2>
        <br>

        <p>
            Climate change can affect our health, ability to grow food, housing, safety and work. 
            Some of us are already more vulnerable<br> to climate
             impacts, such as people living in small island nations and other developing countries.Conditions like sea-level rise and<br> saltwater intrusion have advanced to the point where whole communities have had to relocate, and protracted droughts are putting <br> people at risk of famine. 
             In the future, the number of “climate refugees” is expected to rise.
        </p>
