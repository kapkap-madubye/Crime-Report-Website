        <h3>3.Eat more Vegetables</h3><br>
        <p>
        Eating more vegetables, fruits, whole grains, legumes, nuts and seeds, and less meat and dairy, can significantly lower your 
        environmental impact.<br> Producing
         plant-based foods generally results in fewer greenhouse gas emissions and requires less energy, land and water.
        </p><br>
        <br>
        <h3>4.Throw away less food</h3><br>
        <p>
        When you throw food away, you're also wasting the resources and energy that were used to grow, produce, package, and transport it.<br>
        And when food rots in a landfill, it produces methane, a powerful greenhouse gas. So use what you buy and compost any leftovers.
        </p>
        <br>
        <h3>5.Choose eco-friendly products</h3><br>
        <p>
        Everything we spend money on affects the planet. You have the power to choose which goods and services you support.<br>
        To reduce your environmental impact, buy local and seasonal foods, and choose products from companies who use resources<br>
         responsibly and are committed to cutting their gas emissions and waste.
        </p><br>
        <h3>6.Reduce, reuse, repair & recycle</h3>
        <p><br>
        Electronics, clothes, and other items we buy cause carbon emissions at each point in production, from the extraction of raw<br> 
        materials to manufacturing and transporting goods to market. To protect our climate, buy fewer things, shop second-hand, repair<br>
         what you can, and recycle.
        </p><br>
