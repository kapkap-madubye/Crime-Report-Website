        <h1>Causes of global warming</h1><br>

        <h2>1.Cities and Pollution</h2><br>
        <p>
        Cities are major contributors to climate change.According to UN Habitat, cities consume 78 per cent of the world’s 
        energy and<br> produce more than 60 per cent of greenhouse gas emissions. Yet, they account for less than 2 per cent of the Earth’s surface.
        </p>
        <br>

        <p>
        The sheer density of people relying on fossil fuels makes urban populations highly vulnerable to the effects 
        
         of climate change.<br> Fewer green spaces exacerbate the problem. According to the IPCC report, limiting global warming to 1.5 degrees Celsius would<br>“require
         rapid and far-reaching transitions in uses of energy, land, urban and infrastructure (including transport and buildings), and industrial systems.”
        </p>
        <br>

        <p>
        The effects of climate change are worse among poor and low-income communities, in part because many live on the
         margins of society,<br>in unstable structures, and in areas more susceptible to 
        flooding, landslides, earthquakes, but also because of inadequate capacities, <br>inadequate resources and
         reduced access to emergency response systems. This is even more pronounced in developing countries.
        </p>
        <br>
        <p>
        Pollution, mostly associated as a by-product of urban landscapes, is also linked with climate change.
         Both climate change and air pollution<br> are exacerbated by 
        the burning of fossil fuels, which increase CO<sup>2</sup> emissions, the cause of global warming.
        </p>
        <br>
        <div class="greenhouse-scale">
            <img src="images/Global_Warming.jpg">
        </div>
        <br>
        <h2>2.Deforestation</h2><br>

        <p>
        Deforestation is the clearing, destroying, or otherwise removal of trees through deliberate, natural, or accidental means. It can occur in any area<br> densely populated by trees
         and other plant life, but the majority of it is currently happening in the <a href="https://wwf.panda.org/discover/our_focus/forests_practice/deforestation_fronts_/" target="_blabk">Amazon rainforest</a>.
        </p>

        <br>
        <p>
        Deforestation occurs for a number of reasons, including farming, with 80% of deforestation resulting from extensive 
        cattle ranching, and logging <br>for materials and development. It has been happening for thousands of years, arguably since 
        man began converting from hunter/gatherer to agricultural<br> based societies, and required larger, unobstructed tracks of land 
        to accommodate cattle, crops, and housing. It was only after the onset of the modern <br>era that it became an epidemic.
        </p><br>
        <div class="greenhouse-scale">
            <img src="images/deforestation.jpg">
        </div>
        <br>
        <h3>Environmental Effects of Deforestation From Above</h3>
        <br>
        <h4>Loss of Habitat</h4>
        <br>
        <p>One of the most dangerous and unsettling effects of deforestation is the loss of animal and plant species due 
            to their loss of habitat.<br> 70% of land animals and plant
             species live in forests. Not only does deforestation threaten species known to us, but also those unknown.
        </p><br>
        <p>
        The trees of the rainforest that provide shelter for some species also provide the canopy that regulates the temperature. 
        Deforestation<br> results in a more drastic temperature variation from day to night, much like a desert, which could prove fatal for many inhabitants.
        </p><br>
        <div class="greenhouse-scale">
            <img src="images/def.jpg">
        </div><br>

        <h4>Water in the Atmosphere</h4><br>

        <p>The trees also help control the level of water in the atmosphere by helping to<a style="text-decoration:none; color:green;" href="https://e360.yale.edu/features/how-deforestation-affecting-global-water-cycles-climate-change" target="_blank"> regulate the water cycle</a>.
             In deforested areas, <br>there is less water in the air to be returned to the soil. This then causes dryer 
             soil and the inability to grow crops</p>

        <br><h2>Solutions</h2>  
        <br>
        <h3>1.Save energy at home</h3><br>
        
        <p>
        Much of our electricity and heat is powered by coal, oil and gas. Use less energy by
         lowering your heating and cooling,<br> switching to<strong> LED light bulbs and energy-efficient electric appliances</strong>, 
         washing your laundry with cold water or<br> hanging things to dry instead of using a dryer.
        </p><br>
        <h3>2.Walk,Cycle or take Public Transport</h3><br>
        <p>
        The world’s roads are clogged with vehicles, most of them burning diesel or petrol. Walking or riding a bike instead of driving<br>
         will reduce greenhouse gas emissions – and help your health and fitness. For longer distances, consider taking a train or bus.<br> And carpool whenever possible.<br>
         </p>
