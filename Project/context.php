        <h2>What is Climate change?</h2><br>
        <p>It is the long-term shift in temperatures and weather patterns, to which human activities contribute heavily 
           primarily<br> by burning fossil fuels such as coal,oil, gasoline, and natural gas.
           This results in greenhouse gas emissions hence<br> we experience heat waves, droughts,
           and floods, happening more and frequently and are becoming intense.</p>
           
        <br>

        <p>Burning fossil fuels generates greenhouse gas emissions that act 
        like a blanket wrapped around the Earth, trapping <br>the sun’s heat and raising temperatures.</p>

        <br>

        <p>Examples of greenhouse gas emissions that are causing climate change include carbon dioxide and methane.<br>
            These come from using gasoline for driving a car or coal for heating a building, for example: 
            Clearing land and<br> forests can also release carbon dioxide. Landfills for garbage are a major source of methane emissions.
            Energy,<br> industry, transport, buildings, agriculture and land use are among the main emitters.

       
        </p><br>

        <h2>Greenhouse gas concentrations are at their highest<br>levels in 2 million years</h2><br>

        <p>Emissions continue to rise. As a result, the Earth is now about 1.1°C warmer 
           than it was in the late 1800s.<br> The last decade (2011-2020) was the warmest on record.</p>
        <br>   

        <p>Many people think climate change mainly means warmer temperatures,
           but temperature rise is only the beginning of the story.<br>
           Because the Earth is a system, where everything is connected, changes
            in one area can influence changes in all others.


        </p>

        <br>

        <p>
            The consequences of climate change now include, among others, intense droughts, water scarcity, severe fires,<br> rising 
            sea levels, flooding, melting polar ice, catastrophic storms and declining biodiversity.
        </p>
        <br>
        <div class="greenhouse-scale">
